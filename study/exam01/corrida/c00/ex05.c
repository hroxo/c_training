#include <unistd.h>

void	ft_print_comb(void)
{
	char nbr[3];

	nbr[0] = '0';
	while (nbr[0] < '7' + 1)
	{
		nbr[1] = nbr[0] + 1;
		while (nbr[1] < '8' + 1)
		{
			nbr[2] = nbr[1] + 1;
			while (nbr[2] < '9' + 1)
			{
				write (1, nbr, 3);
				if (!(nbr[0] == '7' && nbr[1] == '8' && nbr[2] == '9'))
					write (1, ", ", 2);
				nbr[2]++;
			}
			nbr[1]++;
		}
		nbr[0]++;
	}
}

int main()
{
	ft_print_comb();
}
